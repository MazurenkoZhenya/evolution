//---------------------------------------------------------------------------------------------------------------------------------
HudScreen* HudScreen::create(IScene* _parent)
{
    if(_parent)
    {
        HudScreen* screen = new HudScreen();
        if(screen && screen->init())
        {
            screen->autorelease();
            _parent->addChild(screen);
            
            return screen;
        }
    }
    
    return NULL;
}

